public class City {

    private int clouds;
    private int hills;
    private int grasslands;
    private int trees;
    private int waterBodies;
    private int houses;
    private int animals;
    private int crops;

    public City() {

    }

    public int getClouds() {
        return clouds;
    }

    public void setClouds(int clouds) {
        this.clouds = clouds;
    }

    public int getHills() {
        return hills;
    }

    public void setHills(int hills) {
        this.hills = hills;
    }

    public int getGrasslands() {
        return grasslands;
    }

    public void setGrasslands(int grasslands) {
        this.grasslands = grasslands;
    }

    public int getTrees() {
        return trees;
    }

    public void setTrees(int trees) {
        this.trees = trees;
    }

    public int getWaterBodies() {
        return waterBodies;
    }

    public void setWaterBodies(int waterBodies) {
        this.waterBodies = waterBodies;
    }

    public int getHouses() {
        return houses;
    }

    public void setHouses(int houses) {
        this.houses = houses;
    }

    public int getAnimals() {
        return animals;
    }

    public void setAnimals(int animals) {
        this.animals = animals;
    }

    public int getCrops() {
        return crops;
    }

    public void setCrops(int crops) {
        this.crops = crops;
    }
}
